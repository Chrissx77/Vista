drop view if exists public.point_view_metrics;
create view public.point_view_metrics
with (security_invoker = true)
as
select
  pv.id,
  pv.created_at,
  pv.name,
  pv.region,
  pv.city,
  pv.description,
  pv.latitude,
  pv.longitude,
  pv.created_by,
  pv.image_urls,
  pv.source,
  pv.external_id,
  coalesce(f.favorite_count, 0)::int as favorite_count,
  coalesce(r.reviews_count, 0)::int as reviews_count,
  case
    when coalesce(r.reviews_count, 0) > 0
      then round(r.avg_rating::numeric, 2)
    when pv.seed_rating is not null
      then round(pv.seed_rating::numeric, 2)
    else null
  end as avg_rating,
  case
    when coalesce(r.reviews_count, 0) > 0
      then coalesce(r.reviews_count, 0)
    else coalesce(pv.seed_rating_count, 0)
  end::int as rating_votes_count
from public.point_views pv
left join (
  select point_view_id, count(*) as favorite_count
  from public.favorites
  group by point_view_id
) f on f.point_view_id = pv.id
left join (
  select
    point_view_id,
    count(*) as reviews_count,
    avg(rating)::double precision as avg_rating
  from public.point_reviews
  group by point_view_id
) r on r.point_view_id = pv.id;

