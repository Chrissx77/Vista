create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists point_reviews_touch_updated_at on public.point_reviews;
create trigger point_reviews_touch_updated_at
before update on public.point_reviews
for each row execute function public.touch_updated_at();

create or replace function public.apply_service_report_threshold()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  vote_count integer;
begin
  select count(*)
  into vote_count
  from public.point_service_change_reports r
  where r.point_view_id = new.point_view_id
    and r.service_id = new.service_id
    and r.suggested_status = new.suggested_status
    and r.processed_at is null;

  if vote_count >= 5 then
    insert into public.point_view_services as pvs (
      point_view_id,
      service_id,
      status,
      updated_by,
      updated_at
    )
    values (
      new.point_view_id,
      new.service_id,
      new.suggested_status,
      null,
      now()
    )
    on conflict (point_view_id, service_id) do update
      set status = excluded.status,
          updated_by = null,
          updated_at = now();

    update public.point_service_change_reports
    set processed_at = now()
    where point_view_id = new.point_view_id
      and service_id = new.service_id
      and suggested_status = new.suggested_status
      and processed_at is null;
  end if;

  return new;
end;
$$;

drop trigger if exists point_service_change_reports_threshold on public.point_service_change_reports;
create trigger point_service_change_reports_threshold
after insert on public.point_service_change_reports
for each row execute function public.apply_service_report_threshold();

